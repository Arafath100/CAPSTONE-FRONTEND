// Toast notification functions using react-toastify

// Warning toast
import { toast } from "react-toastify";

export function toastWarn(text) {
  toast.warn(text, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
}

